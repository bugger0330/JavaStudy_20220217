package a02_변수;

public class Cat {
	public static void main(String[] args) {
//		고양이 모양 출력
		System.out.println("\\    /\\");
		System.out.println(" )  ( ')");
		System.out.println("(  /  )");
		System.out.println(" \\(__)|");
//		\    /\
//		 )  ( ')
//		(  /  )
//		 \(__)|
//		역슬러시 \는 \\ 두번 입력해야 인식된다
		System.out.println();
		System.out.println("|\\_/|");
		System.out.println("|q p|    /}");
		System.out.println("( 0 )\"\"\"\"\\");
		System.out.println("|\"^\"\'    |");
		System.out.println("||_/=\\\\__|");
//		따옴표 출력 하려면 앞에 역슬러시 \ 를 붙이면 된다
		
	}
}
